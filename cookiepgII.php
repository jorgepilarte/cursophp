<h2 align="left">Prestamos</h2>
<h3 width="1000" height="500">Un préstamo es una operación financiera por la cual una persona (prestamista) otorga mediante un contrato o acuerdo entre las partes, un activo (normalmente una cantidad de dinero) a otra persona (prestatario), a cambio de la obtención de un interés (precio del dinero).</h3>

<h4>Los préstamos se consideran una operación financiera de prestación única (principal) y contraprestación múltiple (abono de las cuotas). La amortización, es decir, la devolución paulatina del préstamo, se hará de acuerdo a la duración, interés y acuerdos alcanzados que permitan devolver el principal del préstamo con los intereses.
Los préstamos podemos dividirlos en varias clases en función de su naturaleza. Así pues, diremos que es un préstamo simple si no se pagan periódicamente intereses, o un préstamo con sistema americano si existe el pago periódico de intereses. Existen varios métodos de amortización financiera. Además, en ocasiones los préstamos son de prestación y contraprestación única, ya que se pacta la devolución total con un interés al final de la duración del mismo, es decir, sin el abono de cuotas.
</h4>

<h2>Elementos que conforman un préstamo</h2>

<h3>Estas son los principales conceptos que debemos conocer a la hora de recibir o trabajar con préstamos:</h3>

<h4>•	Capital principal: Es la cantidad de dinero que se ha prestado y sobre la cual se pagará un interés en función de la duración del préstamo y riesgo del adquiriente del préstamo.
•	Interés: Es el coste financiero del préstamo, es decir, el precio del dinero. Es el cargo que se cobra por la utilización de dinero o capital ajeno durante un tiempo, y viene representado en porcentaje sobre el principal.
•	Cuota: Cada uno de los pagos de devolución donde vengan repartidos el principal y el interés.
•	Plazo: Es el tiempo durante el cual se va a utilizar el préstamo. El plazo contará desde que se inicia el contrato hasta que se abone la última cuota, devolviendo así la totalidad del principal y sus intereses.
•	Prestamista: Es el agente que presta el dinero, y al cual debe devolverse junto a unos intereses.
•	Prestatario: Persona que recibe el capital y debe devolverlo conforme a lo pactado, junto a unos intereses.
Tanto el prestamista como el prestatario pueden ser personas físicas o personas jurídicas.
</h4>

<h2>Principales diferencias entre un crédito y un préstamo</h2>

<h4>En materia de banca, un ‘préstamo’ y un crédito, aunque son similares, tienen diferencias. En el crédito, el banco le proporciona al cliente una cuenta, donde el cliente accederá a la cantidad de dinero que necesite y éste suele ir pagando el crédito solicitado periódicamente, con los gastos e intereses añadidos por la entidad.
Por su parte, en un ‘préstamo’, el banco pone a disposición del deudor una cantidad fija de dinero, que debe de ser devuelta, junto a los intereses, en un tiempo predeterminado. Suele ser una operación a medio o largo plazo, que se amortiza en cuotas regulares, a medida que el cliente lo va pagando. Sin embargo, en ambos casos, se trata de la institución bancaria que presta dinero para que en un determinado plazo le sea devuelto junto con unos intereses (principal + intereses).
</h4>
